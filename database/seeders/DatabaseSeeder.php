    {
        User::truncate();

        $faker = \Faker\Factory::create();
        $password = Hash::make('vodafone');

        User::create([
            'name' => 'Administrator',
            'email' => 'admin@vodafone.com',
            'password' => $password,
        ]);

        for ($i = 0; $i < 10; $i++) {
            User::create([
                'name' => $faker->name,
                'email' => $faker->email,
                'password' => $password,
            ]);
        }
    }
}
